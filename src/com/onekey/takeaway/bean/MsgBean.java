package com.onekey.takeaway.bean;


public class MsgBean {
	int errorcode;

	public int getErrorcode() {
		return errorcode;
	}

	public void setErrorcode(int errorcode) {
		this.errorcode = errorcode;
	}

	@Override
	public String toString() {
		return "MsgBean [errorcode=" + errorcode + "]";
	}
	
}
